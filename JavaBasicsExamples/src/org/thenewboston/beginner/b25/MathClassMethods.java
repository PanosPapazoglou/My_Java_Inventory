package org.thenewboston.beginner.b25;

public class MathClassMethods {

  public static void main(String[] args){
  	
  	// Prokeite gia tis 7 pio sinithismenes methodous tis Math Class..
  	
  	//Absolute Value (apolito)
  	System.out.println(Math.abs(-26.7));
  	
  	//Rounds Up (strogilopoiisi pros ta pano)
  	System.out.println(Math.ceil(7.8));
  	
  	//Rounds Down (strogilopoiisi pros ta kato)
  	System.out.println(Math.floor(7.8));  	

  	//Max (epistrefei ton megisto anamesa se 2 arithmous)
  	System.out.println(Math.max(8.6, 5.2));

  	//Min (epistrefei ton elaxisto anamesa se 2 arithmous)
  	System.out.println(Math.min(8.6, 5.2));

  	//Power (epistrefei ton arithmo pou prokiptei otan ton ipsosoume se mia dinami)
  	System.out.println(Math.pow(5, 3));  	
  	
  	//Square Root (epistrefei tin tetragoniki riza enos arithmou)
  	System.out.println(Math.sqrt(9));  	
  	
  }

}
